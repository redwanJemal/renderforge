// ── Bank Implementation ──────────────────────────

function vocabToContentPost(entry: VocabEntry, index: number): ContentPost {
  const colorIndex = index % accentColors.length;
  const accent = accentColors[colorIndex];
  const secondary = secondaryAccents[(index + 3) % secondaryAccents.length];

  return {
    title: `${entry.word} — ${entry.definition}`,
    theme: entry.theme,
    sections: [
      { key: 'word', text: entry.word },
      { key: 'phonetic', text: entry.phonetic },
      { key: 'definition', text: entry.definition },
      ...entry.examples.map((ex, i) => ({
        key: `example-${i + 1}`,
        text: ex.sentence,
      })),
    ],
    fullScript: [
      entry.word,
      entry.phonetic.replace(/[/ˈˌ.]/g, ''),
      entry.definition,
      ...entry.examples.map((e) => e.sentence),
    ].join('. '),
    sceneProps: {
      word: entry.word,
      phonetic: entry.phonetic,
      partOfSpeech: entry.partOfSpeech,
      definition: entry.definition,
      examples: entry.examples,
      synonyms: entry.synonyms,
      antonyms: entry.antonyms,
      level: entry.level,
      dayNumber: index + 1,
      accentColor: accent.color,
      secondaryAccent: secondary,
      bgGradient: accent.bg,
    },
  };
}

