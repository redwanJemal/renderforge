export const vocabEnglishBank: ContentBank = {
  nicheId: 'vocab-english',
  themes: themeList,
  totalPosts: uniqueVocabulary.length,
  segmentKeys: ['word', 'phonetic', 'definition', 'example-1', 'example-2'],
  accentColors,
  entrances: ['fadeIn', 'slideUp', 'scaleIn'],

  getPosts(opts?: { theme?: string; limit?: number }): ContentPost[] {
    let entries = uniqueVocabulary;

    if (opts?.theme) {
      entries = entries.filter((e) => e.theme === opts.theme);
    }

    if (opts?.limit) {
      entries = entries.slice(0, opts.limit);
    }

    return entries.map((entry, i) => vocabToContentPost(entry, i));
  },
};
